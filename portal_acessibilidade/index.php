    <h2 class="text-center">Acessibilidade na Educação</h2>
    <div class="col-md-8">
    <p class="text-justify">O direito a educação é direito de toda criança, adolescente ou adulto, seja ele qual dificuldade tiver. E a constituição afirma isso, que todos somos iguais perante a lei, sem distinção de qualquer natureza. E a educação inclusiva  parte dessa intenção, que cada um possa procurar a plenitude do seu existir, para participar  ativamente na construção de sua vida pessoal, tendo uma existência feliz e de qualidade.</p>
    <p class="text-justify">Primeiramente a escola deve estar adequada àquela criança, jovem ou adulto. Tendo profissionais adequados e espaços direcionados as determinadas deficiências. Essa escola deve também respeitar os limites do educando e desenvolver uma real integração social na comunidade em que vivem. A inclusão implica também em uma mudança de paradigmas, de conceitos e costumes, que fogem as regras tradicionais, ainda fortemente calcados na linearidade do pensamento, no primado do racional e do ensino, na transferência dos conteúdos curriculares.

      Ainda existe uma resistência por parte das escolas, em concretizar essa inclusão, suas desculpas variam entre não ter profissionais especializados, salas adequadas ou acessos dentro das escolas, exemplo para os cadeirantes, entre outras.</p>
    <p class="text-justify">A educação é, portanto um direito de todos, e assegurá-lo é necessariamente, dar boas vindas a esse aluno, sem questionar suas possibilidade ou dificuldades. Respeitando-os, integrando-os ao cotidiano escolar, visando capacitar e melhorar a vida desse educando.</p>
