	<h3>Oculos do Futuro</h3>
	<p>Pessoas com deficiência parcial da visão, especialmente aquelas que têm dificuldade em perceber o ambiente a sua volta, podem agora contar com a ajuda de um par de óculos ultrassônico desenvolvido por Cientistas da Universidade Carlos III, de Madri, Espanha. </p>

<p>O dispositivo pretende auxiliar deficientes visuais parciais na locomoção, ao tornar seus trajetos mais seguros. "Este dispositivo é destinado a pessoas que esbarram em tudo o que elas não conseguem ver por causa da sua perda de campo visual, causada por glaucoma, patologias da retina, etc.", afirma Ricardo Vergaz, chefe do projeto. </p>

<p>O protótipo foi desenvolvido utilizando um dispositivo HMD (Head Mounted Display), um capacete de realidade virtual que inclui duas câmeras ligadas à um computador, onde são processadas todas as img recebidas. </p>
	
