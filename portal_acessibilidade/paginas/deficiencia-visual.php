	<h3>Cinco tecnologias para ajudar pessoas com deficiência visual</h3>
	<p>

<p>Apesar dos avanços no acesso de deficientes visuais a uma vida com maior conforto, a tecnologia móvel ainda tem muito a fazer para deixar os gadgets mais acessíveis às pessoas. É por isso que o TechTudo preparou esta lista com apps, acessórios e sistemas operacionais para cegos e pessoas de pouca visão. Saiba mais sobre esses vários recursos a seguir:  

 </p>



<p><strong>RunKeeper: </strong>esse aplicativo não foi idealizado para deficientes visuais, mas tornou-se uma ótima ferramenta para quem tem problemas de visão e pratica esportes. O RunKeeper funciona com uma assistente que fala todos os detalhes sobre o exercício físico e ainda tem um GPS</p>
    <p>Aplicativo <strong>Be My Eyes</strong> reúne voluntários que emprestam visão a cegos</p>

<p>o Be My Eyes é um aplicativo que une pessoas com a visão perfeita e deficientes visuais. Como uma rede social de solidariedade, esse app de vídeochamadas permite que um usuário de baixa visão ligue a câmera e peça a uma pessoa de qualquer lugar do mundo que descreva o que vê na tela. A rede social tem ainda um sistema de pontos: quanto mais pessoas ajudar, mais pontos ganha quem “empresta a visão”.   </p>
        
    
    <p><strong>iBrailler Notes:</strong> é um app com teclado especial em Braille para iPad, funcionando de forma única, com as teclas indo de encontro aos dedos do usuário, além de ler gestos e outros comandos. </p>
	   
