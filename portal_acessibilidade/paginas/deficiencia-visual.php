	<h1>Acuidade Visual</h1>
	<p>Acuidade Visual (AV) é o grau de aptidão do olho, para discriminar os detalhes espaciais, ou seja, a capacidade de perceber a forma e o contorno dos objetos. Essa capacidade discriminatória é atributos dos cones (células fotossensíveis da retina), que são responsáveis pela Acuidade Visual, central, que compreende a visão de forma e a visão de cores.</p>
    
</div>
    <div class="col-xs-12 col-sm-12 col-md-12 cont-conteudo">
<p>Se os óculos tradicionais, lentes de contacto, ou implantes de lentes intra-oculares não lhe oferecem uma visão nítida, pode dizer que a sua acuidade visual, ou nível de visão é baixo. Não confunda esta condição com cegueira. As pessoas com baixas acuidades visuais ainda têm uma visão útil, que muitas vezes é melhorada com medidas adequadas.</p>  


<p>A visão pode ser dividida em visão central e periférica. A acuidade visual central é medida mostrando-se objetos de diferentes tamanhos a uma distância padrão do olho. Por exemplo, a familiar "Tabela de Snellen", é composta de uma série progressiva de fileiras menores de letras aleatórias usadas para medir a visão a distância.
	</p>
    
