		<h3>Leitor de livros</h3>
         <p>Produtos tecnológicos para deficientes são uma raridade. Talvez pensando nisso, algumas empresas resolveram apostar nesse mercado. Uma delas é a Plustek, que lançou recentemente no Brasil, um scanner chamado Bookreader V200, feito para deficientes visuais.</p>



